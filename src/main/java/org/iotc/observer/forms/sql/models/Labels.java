package org.iotc.observer.forms.sql.models;

public class Labels {
	private String Name;
	private String Val;
	private String Lang;
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getVal() {
		return Val;
	}
	public void setVal(String val) {
		Val = val;
	}
	public String getLang() {
		return Lang;
	}
	public void setLang(String lang) {
		Lang = lang;
	}
	
}
